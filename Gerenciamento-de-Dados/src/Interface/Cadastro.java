    private void jbAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAtualizarActionPerformed
        Pessoa pessoaSelecionada = (Pessoa) jList1.getSelectedValue();
    
         if (pessoaSelecionada != null) {
            pessoaSelecionada.setNome(jtfNome.getText());
            pessoaSelecionada.setCpf(jtfCpf.getText());
            pessoaSelecionada.setCurso(jtfCurso.getText());
            pessoaSelecionada.setEmail(jtfEmail.getText());
            pessoaSelecionada.setIdade(Integer.parseInt(jtfIdade.getText()));

            db.atualizar(pessoaSelecionada);

            preencherLista();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma pessoa na lista para atualizar.");
        }
        preencherLista();
    }//GEN-LAST:event_jbAtualizarActionPerformed

    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        String termoPesquisa = jtfPesquisa.getText().trim();
    
        if (!termoPesquisa.isEmpty()) {
            ArrayList<Pessoa> resultados = db.pesquisar(termoPesquisa);

            listagem.clear();
            for (Pessoa pessoa : resultados) {
                listagem.addElement(pessoa);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Digite um termo de pesquisa.");
        }

    }//GEN-LAST:event_jbPesquisarActionPerformed

